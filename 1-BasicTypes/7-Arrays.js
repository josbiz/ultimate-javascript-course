// Arrays
let animals = ["pig", "horse"];

console.log(animals)

// print an specific position 
console.log(animals[0])

// add a value to a specific position
animals[2] = "dragon"
console.log(animals)


// adding a value having empty slots
animals[10] = "bird"
console.log(animals)

// Arrays are typeof objects
console.log(typeof animals)

// Printing the length of the array
console.log(animals.length)