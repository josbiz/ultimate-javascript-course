// Arithmetic operations

let a = 5
let b = 7

console.log(a + b) // Addition
console.log(a - b) // Substraction
console.log(a * b) // Multiplication
console.log(a / b) // Division
console.log(a % b) // Integer division 
console.log(a ** b) // Power

// Increase value
console.log(++a);
console.log(a++);
console.log(a);

// Decrease value
console.log(--b);
console.log(b-- );
console.log(b);